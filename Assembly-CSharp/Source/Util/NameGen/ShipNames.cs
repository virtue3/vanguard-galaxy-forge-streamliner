		public static string GenerateShipName(Faction faction, SeededRandom seed = null)
		{
			SeededRandom seededRandom = seed ?? SeededRandom.Global;
			if (faction == null || string.IsNullOrEmpty(faction.identifier))
			{
				return seededRandom.Choose<string>(ShipNames.genericNames);
			}
			string[] array;
			if (!ShipNames.factionNameMap.TryGetValue(faction.identifier, out array) || array.Length == 0)
			{
				return seededRandom.Choose<string>(ShipNames.genericNames);
			}
			return seededRandom.Choose<string>(array);
		}

